    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try{
            Producto pro;
            DAOProducto dao = new DAOProducto();
            long clave = Long.parseLong(EntradaId.getText());
            pro=dao.findByID(clave);
            if(pro!=null){
                int pase=JOptionPane.showConfirmDialog(this, "El Producto "+clave+" sera actualizado...", "Mensaje de Confirmaci贸n", JOptionPane.YES_NO_OPTION);
                if(pase==0){
                    pro.setDescripcion(entradaDescripcion.getText());
                    pro.setExistencia(Integer.parseInt(entradaExistencia.getText()));
                    pro.setCosto(new BigDecimal(entradaCosto.getText()));
                    pro.setPrecio(new BigDecimal(entradaPrecio.getText()));
                    dao.update(pro, clave);
                    mensaje("Producto modificado con exito!");
                    vaciarCampos();
                }
            }else{
                mensaje(mensaje1);
            }
        }catch(NumberFormatException ex){
           mensaje(mensaje2);
        }
        try {
            lista();
        } catch (SQLException ex) {
            Logger.getLogger(GUIProducto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed
    
    public void lista() throws SQLException{
        DefaultTableModel model = new DefaultTableModel();
        
        model.addColumn("Id");
        model.addColumn("Descripci贸n");
        model.addColumn("Existencia");
        model.addColumn("Precio ($)");
        model.addColumn("Costo ($)");
        DAOProducto daoProducto=new DAOProducto();
        List<Producto>listaProductos=daoProducto.findAll();
        for(Producto pro:listaProductos){
            model.addRow(new Object[]{String.valueOf(pro.getProductoId()), pro.getDescripcion(),pro.getExistencia(), pro.getPrecio(), pro.getCosto()});
        }
        
        jTable1.setModel(model);

    }
    
    public void vaciarCampos(){
        EntradaId.setText("");
        entradaDescripcion.setText("");
        entradaExistencia.setText("");
        entradaPrecio.setText("");
        entradaCosto.setText("");
    }
    
    public void buscar(){
        try{
            long clave = Long.parseLong(EntradaId.getText());
            DAOProducto daoPro=new DAOProducto();
            Producto pro=daoPro.findByID(clave);
            if(pro!=null){
                entradaDescripcion.setText(pro.getDescripcion());
                entradaExistencia.setText(String.valueOf(pro.getExistencia()));
                entradaPrecio.setText(String.valueOf(pro.getPrecio()));
                entradaCosto.setText(String.valueOf(pro.getCosto()));
            }else{
                mensaje(mensaje1);
            }
        }catch(NumberFormatException ex){
            mensaje(mensaje2);
        }
    }
    
